function renderInputs(el) {
  var n = el.value && parseInt(el.value, 10);
  if (isNaN(n)) {
    return;
  }

  if (n > 6) {
    alert('Maximum 6 persons are allowed.');
    el.value = "";
    cleanDiv("parent");
    return;
  }

  var parentDiv = document.getElementById("parent");
  var currentFields = parentDiv.querySelectorAll(".input-row");
  var currentCount = currentFields.length;

  if (n === currentCount) {
    return;
  }

  if (n > currentCount) {
    // Clear the parent div
    cleanDiv("parent");

    var rowsToAdd = Math.ceil(n / 3);
    var placeholderIndex = 1;
    for (var i = 0; i < rowsToAdd; i++) {
      var rowDiv = document.createElement('div');
      rowDiv.className = 'input-row';
      parentDiv.appendChild(rowDiv);

      var fieldsToAdd = Math.min(n - i * 3, 3);
      for (var j = 0; j < fieldsToAdd; j++) {
        var input = document.createElement('input');
        input.setAttribute('type', 'text');
        input.setAttribute('required', true);
        input.setAttribute('class', 'fullNameInput');
        input.setAttribute('placeholder', 'Full Name ' + placeholderIndex +'*');
        input.setAttribute('id', 'fullnamesponsorpuja');
        input.setAttribute('name', 'fullnamesponsorpuja[]');
        rowDiv.appendChild(input);
        placeholderIndex++;
      }
    }
  } else {
    cleanDiv("parent");
  }
}

